function getProfiles() {
    const data = localStorage.getItem('netflix_profiles');
    if (!data) {
        localStorage.setItem('netflix_profiles', JSON.stringify(defaultProfiles));
        return defaultProfiles;
    }
    return JSON.parse(data);
}

function saveProfiles(profiles) {
    localStorage.setItem('netflix_profiles', JSON.stringify(profiles));
}

function getActiveProfile() {
    const active = localStorage.getItem('netflix_active_profile');
    if (!active) {
        const list = getProfiles();
        localStorage.setItem('netflix_active_profile', JSON.stringify(list[0]));
        return list[0];
    }
    return JSON.parse(active);
}

function setActiveProfile(profile) {
    localStorage.setItem('netflix_active_profile', JSON.stringify(profile));
    window.dispatchEvent(new Event('profilechanged'));
}

