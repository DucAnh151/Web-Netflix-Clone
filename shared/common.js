// ===== KIỂM TRA ĐĂNG NHẬP =====
function checkAuth() {
    const session = sessionStorage.getItem('netflix_user') || localStorage.getItem('netflix_remember');
    if (!session) {
        // Tìm đường dẫn phù hợp về trang login
        const path = window.location.pathname;
        if (path.includes('/trang_chu/') || path.includes('/xem_phim/') || path.includes('/ho_so/') || path.includes('/cai_dat/') || path.includes('/tro_giup/')) {
            window.location.href = '../login/login.html';
        } else {
            window.location.href = './login/login.html';
        }
        return null;
    }
    return JSON.parse(session);
}

// ===== THIẾT LẬP THEME SÁNG / TỐI =====
function initTheme() {
    const savedTheme = localStorage.getItem('netflix_theme') || 'dark';
    setTheme(savedTheme);
}

function setTheme(theme) {
    document.documentElement.setAttribute('data-theme', theme);
    const themeIcon = document.getElementById('theme-icon');
    if (themeIcon) {
        themeIcon.textContent = theme === 'dark' ? '☀️' : '🌙';
    }
    localStorage.setItem('netflix_theme', theme);
}

function toggleTheme() {
    const current = document.documentElement.getAttribute('data-theme') || 'dark';
    setTheme(current === 'dark' ? 'light' : 'dark');
}

// ===== THIẾT LẬP NGÔN NGỮ (VI/EN) =====
function getLanguage() {
    return localStorage.getItem('netflix_lang') || 'vi';
}

function setLanguage(lang) {
    localStorage.setItem('netflix_lang', lang);
    translatePage();
    // Dispatch event để thông báo cho trang chủ hoặc trang khác load lại dữ liệu tương ứng
    window.dispatchEvent(new Event('languagechanged'));
}

function translatePage() {
    const lang = getLanguage();
    const transElements = document.querySelectorAll('[data-translate]');
    transElements.forEach(el => {
        const key = el.getAttribute('data-translate');
        if (dictionary[lang] && dictionary[lang][key]) {
            if (el.tagName === 'INPUT' && el.placeholder) {
                el.placeholder = dictionary[lang][key];
            } else {
                el.textContent = dictionary[lang][key];
            }
        }
    });
}

// ===== QUẢN LÝ DANH SÁCH YÊU THÍCH (MY LIST) =====
function getMyList() {
    const list = localStorage.getItem('netflix_mylist');
    return list ? JSON.parse(list) : [];
}

function addToMyList(filmId) {
    let list = getMyList();
    if (!list.includes(filmId)) {
        list.push(filmId);
        localStorage.setItem('netflix_mylist', JSON.stringify(list));
        window.dispatchEvent(new Event('mylistchanged'));
        return true;
    }
    return false;
}

function removeFromMyList(filmId) {
    let list = getMyList();
    list = list.filter(id => id !== filmId);
    localStorage.setItem('netflix_mylist', JSON.stringify(list));
    window.dispatchEvent(new Event('mylistchanged'));
}

function toggleMyList(filmId) {
    if (isInMyList(filmId)) {
        removeFromMyList(filmId);
        return false;
    } else {
        addToMyList(filmId);
        return true;
    }
}

function isInMyList(filmId) {
    return getMyList().includes(filmId);
}

